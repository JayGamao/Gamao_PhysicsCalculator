package mcm.edu.ph.gamao_physicscalculator.Model;

public class gs {
    double radius = 0;
    double base = 0;
    double height = 0;
    double majoraxis = 0;
    double minoraxis = 0;
    double length = 0;
    double width = 0;
    double side = 0;
    int formulas;
    int save;

    double answer = 0;
    String fillblanks;
    String whatshape;


    public gs(){}
    public double getradius() { return radius; }
    public void setradius(double radius) { this.radius = radius; }

    public double getbase() { return base; }
    public void setbase(double base) { this.base = base; }

    public double getheight() { return height; }
    public void setheight(double height) { this.height = height; }

    public double getmajoraxis() { return majoraxis; }
    public void setmajoraxis(double majoraxis) { this.majoraxis = majoraxis; }

    public double getminoraxis() { return minoraxis; }
    public void setminoraxis(double minoraxis) { this.minoraxis = minoraxis; }

    public double getlength() { return length; }
    public void setlength(double length) { this.length = length; }

    public double getwidth() { return width; }
    public void setwidth(double width) { this.width = width; }

    public double getside() { return side; }
    public void setside(double side) { this.side = side; }

    public int getformulas() { return formulas; }
    public void setformulas(int formulas) { this.formulas = formulas; }

    public double getanswer() { return answer; }
    public void setanswer(double answer) { this.answer = answer; }

    public String getfillblanks() { return fillblanks; }
    public void setfillblanks(String fillblanks) { this.fillblanks = fillblanks; }

    public String getwhatshape() { return whatshape; }
    public void setwhatshape(String whatshape) { this.whatshape = whatshape; }

    public int getsave() { return save; }
    public void setsave(int save) { this.save = save; }




}
